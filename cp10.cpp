#include<iostream>
#include<bits/stdc++.h>
using namespace std;
int main(){
int t,n,sum,cunter=0;
cin>>t>>n;
while(t--){
   cin>>sum;
if(sum<=n){
cunter++;
} 
else{
   cunter--;
}
}

cout<<cunter<<endl;
return 0;
}


